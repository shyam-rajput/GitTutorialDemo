// Unstaging & Unmodifying Files In Git:

// 1) git restore --staged file_name : Kisi bhi file ko stage se unstage karne ke liye is command ka use karte hai.

// 2) git checkout -- file_name : Agar aapne kisi ek file me kuch change kar diya hai matlab file ko modified kar diya hai and then aapko lagta hai ki kuch galat ho gaya to is command ka use karne se ham pichla wala jo commit haga us par wapas chale jaayenge, or jo naye changes kiye honge bho remove ho jaayenge.

// git checkout -- f : Is command ka use karne se ham sabhi files me jo pichla wala commit hoga us par wapas chale jaayenge, or jo naye changes kiye honge bho remove ho jaayenge (matlab agar aapko apne modified changes remove karke last waale commit me wapas jana hai tab is command ka us kare).
